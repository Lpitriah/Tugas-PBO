a= 10
b=  5

hasil_penjumlahan= a+b
print ("hasil penjumlahan =", hasil_penjumlahan)

hasil_perkalian = a*b
print ("hasil perkalian =", hasil_perkalian)

hasil_pangkat = a**b
print ("hasil pangkat =",hasil_pangkat)

hasil_modulus = a % b
print ("hasil modulus =", hasil_modulus)

abs_a = abs(a)
print(f"Nilai absolut dari a = {abs_a}")

comparison_result = (a > b) - (a < b)
print(f"Hasil perbandingan a dan b = {comparison_result}")

max_value = max(a, b)
print(f"Nilai terbesar dari a dan b = {max_value}")

min_value = min(a, b)
print(f"Nilai terkecil dari a dan b = {min_value}")
